package aula7.Ex2;

public class Pixel {
	
	private byte R;
	private byte G;
	private byte B;
	
	//Construtor 
	public Pixel(byte R, byte G, byte B)
	{
		this.R=R;
		this.G=G;
		this.B=B;
	}
	
	public byte R() { return R; }
	public byte G() { return G; }
	public byte B() { return B; }

}
